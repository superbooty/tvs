
package com.ag.tvs.data;

public class DisplayablePrice {
    
    private String dollars = null;
    private String cents = null;
    
    public DisplayablePrice() {}
    
    public String getDollars() {
        return dollars;
    }
    
    public void setDollars(String dollars) {
        this.dollars = dollars;
    }
    
    public String getCents() {
        return cents;
    }
    
    public void setCents(String cents) {
        this.cents = cents;
    }

}